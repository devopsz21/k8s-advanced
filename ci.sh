#!/bin/bash

set -e

DIR=$(cd "$(dirname "$0")"; pwd -P)

FILES=$1/*.sh
for f in $FILES
do
  if echo "$f" | grep "-psp\.sh"; then
      echo
      echo "-------------------------------------------------------------------"
      echo "NOT processing $f"
      echo "-------------------------------------------------------------------"
  elif
      echo
      echo "-------------------------------------------------------------------"
      echo "Processing $f"
      echo "-------------------------------------------------------------------"
      sh -c "$f"
  fi
done
